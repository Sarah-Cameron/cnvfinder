#!/bin/python3

import sys
import subprocess
from collections import OrderedDict

def fasta_to_dict(fasta_file):
    """
    Convert a FASTA file to a dictionary where keys are headers and values 
    are tuples of (sequence length, "A").
    """
    fasta_dict = OrderedDict()
    try:
        with open(fasta_file, 'r') as file:
            header = None
            sequence = []
            for line in file:
                line = line.strip()
                if line.startswith('>'):
                    if header:
                        # Combine the sequence list into a single string
                        full_sequence = ''.join(sequence)
                        # Store the length of the sequence and the static string "A" as a tuple
                        fasta_dict[header] = (len(full_sequence), "A")
                    # Start a new sequence
                    header = line[1:].split()[0]  # remove '>'
                    sequence = []
                else:
                    sequence.append(line)
            # Don't forget to add the last sequence
            if header:
                full_sequence = ''.join(sequence)
                fasta_dict[header] = (len(full_sequence), "A")
    except FileNotFoundError:
        print(f"Error: File '{fasta_file}' not found.", file=sys.stderr)
        sys.exit(1)
    except Exception as e:
        print(f"An error occurred: {e}", file=sys.stderr)
        sys.exit(1)
    return fasta_dict

def generate_reference_genomes(fasta_dict, name, species, gc_file):
    """
    Generate the reference genome dictionary in the required format.
    
    Parameters:
        fasta_dict (OrderedDict): Dictionary generated by fasta_to_dict with chromosome names and lengths.
        name (str): The name of the reference genome (e.g., "MGSCv37").
        species (str): The species name (e.g., "Mus musculus").
        gc_file (str): Path to the GC content file.
        
    Returns:
        dict: A dictionary with reference genome information.
    """
    return {
	name: {
            "name": name,
            "species": species,
            "chromosomes": fasta_dict,  # Use the dictionary generated from FASTA
            "gc_file": gc_file
        }
    }

if __name__ == "__main__":
    # Ensure the script is called with the correct number of arguments
    if len(sys.argv) != 5:
        print("Usage: python script.py <fasta_file_path> <name> <species> <gc_file_path>", file=sys.stderr)
        sys.exit(1)

    # Get the inputs from the command-line arguments
    fasta_file_path = sys.argv[1]
    genome_name = sys.argv[2]
    species_name = sys.argv[3]
    gc_file_path = sys.argv[4]
    
    # Call the function to process the FASTA file and convert it to a dictionary
    fasta_lengths_with_a = fasta_to_dict(fasta_file_path)

    # Generate the reference genome dictionary using the provided variables
    reference_genomes = generate_reference_genomes(fasta_lengths_with_a, genome_name, species_name, gc_file_path)

    # Print the final dictionary for reference genomes
    print(f"import_reference_genomes = {reference_genomes}")

